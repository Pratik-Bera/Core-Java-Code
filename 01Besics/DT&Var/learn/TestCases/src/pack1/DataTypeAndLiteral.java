package pack1;
class DataTypeAndLiteral{
    public static void main(String[] args){
        int i1=34;
        System.out.println("The Value of i1 int type :"+i1);
        double d1=45.5;
        System.out.println("The Value of d1 int type :"+d1);
        //float f1=25.5;  //possible lossy conversion from double to float.
        float f1=15.5f;
        System.out.println("The Value of f1 float type :"+f1);
        boolean b1=true;
        System.out.println("The Value of b1 float type :"+b1);


    }
}